<p>The Thomas Ash website is still up and not looking it's age of eight years. It was built in Flash for a design agency that was still learning how to transfer their excellent design skills to building websites. Managing the relationship with a design team that are primarily print based just takes listening on both sides, and within a short time we'd planned a Flash website that kept the download time low whilst reflecting the quality of the brand. The finished article took into account non-flash fallbacks and SEO friendly content, all the time looking slick and simple.</p>


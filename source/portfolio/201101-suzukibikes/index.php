<p>After running any large website for a few years, it's easy to lose sight of your goals. The Suzuki GB Motorcycles homepage was becoming a billboard for sales and marketing offers, with the normal pressure to have every message maximised. The problem was that the passion for biking was not clearly evident amongst all the finance offers and promotions.</p>

<p>We took it upon ourselves to try and help regain that passion, to update the massive website with larger spreads, product pages with more impact, and above all, a homepage that made you want to jump on a bike, not reach for a calculator.</p>

<p>But the marketing messages? They couldn't be lost, so I produced a design that meant they didn't need to compete for the same real estate. They appeared from behind sliding panels, giving a homepage with impact and value, whilst staying maintainable and a valuable place to advertise.</p>


<p>After leaving Banner Corporation in Chelsea, I took on a six month contract building a website and extranet for Engenuity, a high tech engineering analysis company.</p>

<p>That site - despite being built to fit on the minimum required 640x480px screen (common at the time) - is still live today. The extranet is also still running, securely uploading and downloading information and results to Engenuity's clients. It was my first taste of managing my own project from start to finish, and coupled with a client with exciting ideas to push me further, I didn't want it to be my last.</p>


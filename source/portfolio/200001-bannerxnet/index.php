<p>At the start it was called "web publishing" - the idea that an HTML site could be created from a "script" and a "database". I started playing around with these concepts with the now defunct "Tango" development platform that hooked into Filemaker Pro databases.</p>

<p>It wasn't long before we were creating Extranets for Banner, allowing clients to remotely download design concepts and media schedules, whilst I designed workflows for administrators to scan press clippings and post them up on a secure website (before clippings services saw the potential in the web). It's amazing to think about what we achieved so early on.</p>

